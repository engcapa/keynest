#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');
const distDir = path.join(root, 'dist');
const outFile = path.join(root, 'server', 'web-bundle.generated.ts');

const MIME = {
  '.html': 'text/html; charset=utf-8',
  '.js':   'application/javascript; charset=utf-8',
  '.mjs':  'application/javascript; charset=utf-8',
  '.css':  'text/css; charset=utf-8',
  '.json': 'application/json; charset=utf-8',
  '.map':  'application/json; charset=utf-8',
  '.svg':  'image/svg+xml',
  '.png':  'image/png',
  '.jpg':  'image/jpeg',
  '.jpeg': 'image/jpeg',
  '.gif':  'image/gif',
  '.webp': 'image/webp',
  '.ico':  'image/x-icon',
  '.woff': 'font/woff',
  '.woff2':'font/woff2',
  '.ttf':  'font/ttf',
  '.otf':  'font/otf',
  '.txt':  'text/plain; charset=utf-8',
};

function walk(dir) {
  const out = [];
  for (const e of fs.readdirSync(dir, { withFileTypes: true })) {
    const p = path.join(dir, e.name);
    if (e.isDirectory()) out.push(...walk(p));
    else out.push(p);
  }
  return out;
}

const bundle = {};
if (fs.existsSync(distDir)) {
  for (const abs of walk(distDir)) {
    const rel = '/' + path.relative(distDir, abs).split(path.sep).join('/');
    const ext = path.extname(abs).toLowerCase();
    const type = MIME[ext] || 'application/octet-stream';
    const data = fs.readFileSync(abs).toString('base64');
    bundle[rel] = { type, data };
  }
  console.log(`[bundle-web] packed ${Object.keys(bundle).length} files from ${distDir}`);
} else {
  console.log(`[bundle-web] dist/ missing at ${distDir} — writing empty bundle`);
}

const header = '// AUTO-GENERATED by scripts/bundle-web.mjs — do not edit by hand.\n';
const body = `export const WEB_BUNDLE: Record<string, { type: string; data: string }> = ${JSON.stringify(bundle)};\n`;
fs.mkdirSync(path.dirname(outFile), { recursive: true });
fs.writeFileSync(outFile, header + body);
console.log(`[bundle-web] wrote ${outFile} (${(fs.statSync(outFile).size / 1024 / 1024).toFixed(2)} MB)`);
